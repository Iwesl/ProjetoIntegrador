    public void listarVeiculo() {
        try {
            VeiculoDAO vDAO = new VeiculoDAO(); // instancia a classe ProdutoDB()
            ArrayList<Veiculo> v = vDAO.consultaVeiculoByID(Integer.parseInt(frmID.getText().trim())); // coloca o método dentro da variável

            String modelo;
            String marca;
            String placa;
            String cor;
            int id_cliente;

            for (int i = 0; i < v.size(); i++) {
                // loop que pega os dados e insere na tabela

                modelo = v.get(i).getModelo_veiculo();
                marca = v.get(i).getMarca_veiculo();
                placa = v.get(i).getPlaca_veiculo();
                cor = v.get(i).getCor_veiculo();
                id_cliente = v.get(i).getId_cliente();
                // pega os dados salvos do banco de dados (que estão nas variáveis) e os coloca nos objetos definidos

                frmModelo.setText(modelo);
                frmMarca.setText(marca);
                frmPlaca.setText(placa);
                frmCor.setText(cor);
                frmCliente.setText(String.valueOf(id_cliente));
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro no processo!\n" + ex.getMessage(), "Consultas", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            Logger.getLogger(frmPesquisarVeiculo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void removerVeiculo(){
         VeiculoDAO vDAO = new VeiculoDAO();
        Veiculo vei = new Veiculo();
        if (frmID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Remover Veículos", JOptionPane.WARNING_MESSAGE);
        } else if (frmPlaca.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Para evitar exclusões indesejadas, faça uma consulta do veículo antes.", "Remover Veículos", JOptionPane.WARNING_MESSAGE);
        } else {
            if (JOptionPane.showConfirmDialog(this, "Você tem certeza que deseja remover este veículo do Banco de Dados?", "ATENÇÃO",
                    JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                vei.setPlaca_veiculo(frmPlaca.getText());
                try {
                    vDAO.removeVeiculoByID(Integer.parseInt(frmID.getText()));
                    if(!vDAO.verificaPlaca(vei)){
                        JOptionPane.showMessageDialog(this, "Remoção efetuada com sucesso!", "Remover Veículos", JOptionPane.INFORMATION_MESSAGE);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(frmPesquisarVeiculo.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                // não faz nada
            }
        }
    }

